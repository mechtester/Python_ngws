# Be-sides assigning a variable an initial value, we can also perform the usual mathematical
# operations on variables.

# Basic Operators
#  addition :  +
#  subtraction :  -
#  multiplication : *
#  division : /
#  floor division :  //  (rounds down the answer to the nearest whole number)
#  modulus :  %  (gives the remainder when 5 is divided by 2)
#  exponent : **  (5 to the power of 2)

x=5
y=2

print("Additon:",x+y)
print("Subtraction:",x-y)
print("multiplication:",x*y)
print("divison:",x/y)
print("Floor division:",x//y)
print("Modulus:",x%y)
print("Eponent:",x**y)

